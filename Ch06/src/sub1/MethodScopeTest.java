public class MethodScopeTest {
	
	// 전역변수
	static int result1 = 0;
	
	public static void main(String[] args) {
		
		// 지역변수
		// - 지역변수는 해당 영역(scope)에서만 참조 가능하다.
		int start = 1;
		int end = 10;
		int result = 0;
		
		result = sum(start, end);
		
		System.out.println("result : " + result);
		
		
	} // main end

	public static int sum(int s, int e) {
		
		// 지역변수
		int sum = 0;
		
		for(int k = s; k <= e; k++) {
			sum += k;
		}
		
		return sum;
	}

}
